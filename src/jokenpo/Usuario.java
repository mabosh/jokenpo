package jokenpo;

public class Usuario {

	private String nome;
	private String escolha;
	
	public void setEscolha(String escolha) {
		this.escolha = escolha;	
	}

	public String getEscolha() {
		return this.escolha;
	}

	public String getNome() {
		return this.nome;
	}

	public void setNome(String nome) {		
		this.nome = nome;
	}


}